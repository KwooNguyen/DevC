/******************************************************************************************************************//**
 * Read sensor data
 *********************************************************************************************************************/
sensor_data_setting_t read_sensors()
{
    sensor_data_setting_t data;

    /* Initialize random number generation program */
    srand(time(NULL));

    /* Select random number from -20 to 110 */
    int randomNumbersoilmoisture = ((rand() % 131) - 20);

    /* Select random number from 0 to 50 */
    int randomNumbertemperatureenv = rand() % 51;

    /* Return read value from sensors */
    data.soil_moisture = randomNumbersoilmoisture;
    data.temperature_env = randomNumbertemperatureenv;

    return data;
}